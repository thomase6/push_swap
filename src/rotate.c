#include "push_swap.h"

static void	rotate(t_stack_node **stack)
{
	t_stack_node	*end;
	t_stack_node	*first;

	if (!*stack || !(*stack)->next)
		return ;
	end = find_end(*stack);
	first = *stack;
	*stack = first->next;
	(*stack)->prev = NULL;
	end->next = first;
	first->prev = end;
	first->next = NULL;
}

void	ra(t_stack_node **a)
{
	rotate(a);
	ft_printf("ra\n");
}

void	rb(t_stack_node **b)
{
	rotate(b);
	ft_printf("rb\n");
}

void	rr(t_stack_node **a, t_stack_node **b)
{
	rotate(a);
	rotate(b);
	ft_printf("rr\n");
}